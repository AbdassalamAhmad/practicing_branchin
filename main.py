# add two numbers
def adding(x,y):
    return x+y

# subtract two numebrs
def minus(x,y):
    return x-y

# multiply x by y
def multiply(x,y):
    return x*y

# divide x by y
def divide(x,y):
    return x/y

x = int(input("x: ")) # first argument
y = int(input("y: ")) # second argument

print (f"x+y = {adding(x,y)}")
print (f"x-y = {minus(x,y)}")
print (f"x*y = {multiply(x,y)}")
print (f"x/y = {divide(x,y)}")


